try:
    x = int(input("Введите координату X точки: "))
    y = int(input("Введите координату Y точки: "))
    x1 = int(input("Введите координату левой верхней точки X1: "))
    y1 = int(input("Введите координату левой верхней точки Y1: "))
    x2 = int(input("Введите координату правой нижней точки X2: "))
    y2 = int(input("Введите координату правой нижней точки Y2: "))

    print(x1 <= x <= x2 and y1 <= y <= y2)
except ValueError:
   print("Ошибка! Неправильный тип данных.")